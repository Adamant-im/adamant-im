<template>
  <v-bottom-navigation
    v-model="currentPageIndex"
    app
    height="50"
    class="app-navigation"
    :elevation="0"
  >
    <!-- Wallet -->
    <v-btn
      to="/home"
    >
      <v-icon
        icon="mdi-wallet"
      />
      <span>{{ $t('bottom.wallet_button') }}</span>
    </v-btn>

    <!-- Chat -->
    <v-btn
      to="/chats"
    >
      <v-badge
        v-if="numOfNewMessages > 0"
        :value="numOfNewMessages"
        overlap
        color="primary"
        :content="numOfNewMessages > 99 ? '99+' : numOfNewMessages"
      >
        <v-icon
          icon="mdi-forum"
        />
      </v-badge>
      <v-icon
        v-else
        icon="mdi-forum"
      />

      <span>{{ $t('bottom.chats_button') }}</span>
    </v-btn>

    <!-- Settings -->
    <v-btn
      to="/options"
    >
      <v-icon
        icon="mdi-cog"
      />
      <span>{{ $t('bottom.settings_button') }}</span>
    </v-btn>
  </v-bottom-navigation>
</template>

<script>
export default {
  data: () => ({
    pages: [
      {
        title: 'wallet',
        link: '/home',
        icon: 'mdi-wallet'
      },
      {
        title: 'chats',
        link: '/chats',
        icon: 'mdi-forum'
      },
      {
        title: 'settings',
        link: '/options',
        icon: 'mdi-cog'
      }
    ],
    currentPageIndex: 0
  }),
  computed: {
    numOfNewMessages () {
      return this.$store.getters['chat/totalNumOfNewMessages']
    }
  },
  watch: {
    '$route.path' () {
      this.currentPageIndex = this.getCurrentPageIndex()
    }
  },
  mounted () {
    this.currentPageIndex = this.getCurrentPageIndex()
  },
  methods: {
    getCurrentPageIndex () {
      const currentPage = this.pages.find(page => {
        const pattern = new RegExp(`^${page.link}`)

        return this.$route.path.match(pattern)
      })

      return this.pages.indexOf(currentPage)
    }
  }
}
</script>

